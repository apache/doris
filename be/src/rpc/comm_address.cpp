#include "compat.h"
#include "comm_address.h"

namespace palo {

std::string CommAddress::to_str() const {
    if (m_type == PROXY) {
        return proxy;
    } else if (m_type == INET) {
        return InetAddr::format(inet);
    }
    return "[NULL]";

}

} //namespace palo
