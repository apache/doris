suite('push_down_filter_through_partition_topn_with_unique_function') {
    sql 'SET enable_nereids_planner=true'
    sql 'SET runtime_filter_mode=OFF'
    sql 'SET enable_fallback_to_original_planner=false'
    sql "SET ignore_shape_nodes='PhysicalDistribute'"
    sql "SET detail_shape_nodes='PhysicalProject'"
    sql 'SET disable_nereids_rules=PRUNE_EMPTY_PARTITION'
    // partition topn is generated by a window-then-filter (rn <= K) pattern; enable the rule.
    sql 'SET enable_partition_topn=true'

    // rand() > 0.5 has empty input slots; old code would push it below partition top-N
    // because `partitionKeySlots.containsAll(emptySet) = true`. That replaces
    // "topN then random filter" with "random filter then topN", changing topN membership.
    qt_filter_through_partition_topn_unique_1 '''
        explain shape plan
        select * from (
            select id, msg, row_number() over (partition by id order by msg) rn from t1
        ) v
        where rn <= 3 and rand() > 0.5
    '''

    // Predicate uses the partition key together with a unique function; legacy check matched.
    qt_filter_through_partition_topn_unique_2 '''
        explain shape plan
        select * from (
            select id, msg, row_number() over (partition by id order by msg) rn from t1
        ) v
        where rn <= 3 and id + rand(1, 100) > 5
    '''
}
