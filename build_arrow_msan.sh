# Rebuild Arrow (and its bundled deps like Brotli) with MSAN-instrumented libc++.
# This replaces the pre-built Arrow .a files in thirdparty/installed/ with
# MSAN-compatible versions that use libc++ instead of libstdc++.
#
# Prerequisites:
#   1. Run `bash build.sh --be --msan` at least once to generate the libc++ headers
#      in be/build_MSAN/libcxx-msan/include/c++/v1/
#   2. Run `bash thirdparty/build-thirdparty.sh` at least once to install deps
#
# Usage:
#   bash build_arrow_msan.sh 2>&1 | tee msan_arrow_rebuild.log

set -eo pipefail

DORIS_HOME="$(cd "$(dirname "${BASH_SOURCE[0]}")" &>/dev/null && pwd)"
TP_DIR="${DORIS_HOME}/thirdparty"
TP_INSTALL_DIR="${TP_DIR}/installed"
TP_SOURCE_DIR="${TP_DIR}/src"

# Source env for CC/CXX
if [[ -f "${DORIS_HOME}/env.sh" ]]; then
    export DO_NOT_CHECK_JAVA_ENV=1
    . "${DORIS_HOME}/env.sh"
    export DO_NOT_CHECK_JAVA_ENV=
fi

# Source vars for package names
. "${TP_DIR}/vars.sh"

# Compiler must be clang
if [[ "${CC}" != *clang* ]]; then
    echo "ERROR: MSAN requires clang. Current CC=${CC}"
    exit 1
fi

# libc++ include paths (generated by BE MSAN cmake build)
LIBCXX_GEN_INCLUDE="${DORIS_HOME}/be/build_MSAN/libcxx-msan/include/c++/v1"
LIBCXX_SRC_INCLUDE="${TP_SOURCE_DIR}/llvm-project-20.1.7/libcxx/include"
LIBCXXABI_SRC_INCLUDE="${TP_SOURCE_DIR}/llvm-project-20.1.7/libcxxabi/include"

if [[ ! -f "${LIBCXX_GEN_INCLUDE}/__config_site" ]]; then
    echo "ERROR: libc++ headers not found at ${LIBCXX_GEN_INCLUDE}"
    echo "Run 'bash build.sh --be --msan' first to generate them."
    exit 1
fi

echo "=== Rebuilding Arrow with MSAN libc++ ==="
echo "CC=${CC}"
echo "CXX=${CXX}"
echo "LIBCXX_GEN_INCLUDE=${LIBCXX_GEN_INCLUDE}"

# MSAN libc++ CXXFLAGS: use libc++ headers instead of libstdc++
MSAN_CXXFLAGS="-stdlib=libc++ -nostdinc++ -isystem${LIBCXX_GEN_INCLUDE} -isystem${LIBCXX_SRC_INCLUDE} -isystem${LIBCXXABI_SRC_INCLUDE}"

# MSAN sanitizer flags (same as BE cmake)
MSAN_SANITIZER_FLAGS="-fsanitize=memory -fsanitize-memory-track-origins=2 -fno-omit-frame-pointer"

PARALLEL="$(($(nproc) / 4 + 1))"
CMAKE_CMD="cmake"
BUILD_SYSTEM="ninja"
GENERATOR="Ninja"

echo "=== Building Arrow ==="

ARROW_SOURCE="arrow-apache-arrow-17.0.0"
check_if_source_exist() { [[ -d "${TP_SOURCE_DIR}/$1" ]] || { echo "Source $1 not found"; exit 1; }; }
check_if_source_exist "${ARROW_SOURCE}"

cd "${TP_SOURCE_DIR}/${ARROW_SOURCE}/cpp"

# Clean previous build
rm -rf release_msan
mkdir -p release_msan
cd release_msan

export ARROW_BROTLI_URL="${TP_SOURCE_DIR}/${BROTLI_NAME}"
export ARROW_GLOG_URL="${TP_SOURCE_DIR}/${GLOG_NAME}"
export ARROW_LZ4_URL="${TP_SOURCE_DIR}/${LZ4_NAME}"
export ARROW_FLATBUFFERS_URL="${TP_SOURCE_DIR}/${FLATBUFFERS_NAME}"
export ARROW_ZSTD_URL="${TP_SOURCE_DIR}/${ZSTD_NAME}"
export ARROW_Thrift_URL="${TP_SOURCE_DIR}/${THRIFT_NAME}"
export ARROW_SNAPPY_URL="${TP_SOURCE_DIR}/${SNAPPY_NAME}"
export ARROW_ZLIB_URL="${TP_SOURCE_DIR}/${ZLIB_NAME}"
export ARROW_XSIMD_URL="${TP_SOURCE_DIR}/${XSIMD_NAME}"
export ARROW_ORC_URL="${TP_SOURCE_DIR}/${ORC_NAME}"
export ARROW_GRPC_URL="${TP_SOURCE_DIR}/${GRPC_NAME}"
export ARROW_PROTOBUF_URL="${TP_SOURCE_DIR}/${PROTOBUF_NAME}"

# Combined CXX flags: MSAN sanitizer + libc++ headers
CXXFLAGS="${MSAN_SANITIZER_FLAGS} ${MSAN_CXXFLAGS}"
# C flags: just MSAN sanitizer (no libc++ for C code)
CFLAGS="${MSAN_SANITIZER_FLAGS}"

echo "CXXFLAGS=${CXXFLAGS}"
echo "CFLAGS=${CFLAGS}"

LDFLAGS="-L${TP_INSTALL_DIR}/lib -L${TP_INSTALL_DIR}/lib64"

CFLAGS="${CFLAGS}" CXXFLAGS="${CXXFLAGS}" LDFLAGS="${LDFLAGS}" \
    "${CMAKE_CMD}" -G "${GENERATOR}" \
    -DCMAKE_C_COMPILER="${CC}" \
    -DCMAKE_CXX_COMPILER="${CXX}" \
    -DCMAKE_C_FLAGS="${CFLAGS}" \
    -DCMAKE_CXX_FLAGS="${CXXFLAGS}" \
    -DARROW_PARQUET=ON -DARROW_IPC=ON -DARROW_BUILD_SHARED=OFF \
    -DARROW_BUILD_STATIC=ON -DARROW_WITH_BROTLI=ON -DARROW_WITH_LZ4=ON -DARROW_USE_GLOG=ON \
    -DARROW_WITH_SNAPPY=ON -DARROW_WITH_ZLIB=ON -DARROW_WITH_ZSTD=ON -DARROW_JSON=ON \
    -DARROW_WITH_UTF8PROC=OFF -DARROW_WITH_RE2=ON -DARROW_ORC=ON \
    -DARROW_COMPUTE=ON \
    -DARROW_FILESYSTEM=ON \
    -DARROW_DATASET=ON \
    -DARROW_ACERO=ON \
    -DCMAKE_INSTALL_PREFIX="${TP_INSTALL_DIR}" \
    -DCMAKE_INSTALL_LIBDIR=lib64 \
    -DARROW_BOOST_USE_SHARED=OFF \
    -DARROW_WITH_GRPC=ON \
    -DgRPC_SOURCE=SYSTEM \
    -DgRPC_ROOT="${TP_INSTALL_DIR}" \
    -DARROW_WITH_PROTOBUF=ON \
    -DProtobuf_SOURCE=SYSTEM \
    -DProtobuf_LIB="${TP_INSTALL_DIR}/lib/libprotoc.a" -DProtobuf_INCLUDE_DIR="${TP_INSTALL_DIR}/include" \
    -DARROW_FLIGHT=ON \
    -DARROW_FLIGHT_SQL=ON \
    -DBoost_USE_STATIC_RUNTIME=ON \
    -DARROW_GFLAGS_USE_SHARED=OFF \
    -Dgflags_ROOT="${TP_INSTALL_DIR}" \
    -Dglog_ROOT="${TP_INSTALL_DIR}" \
    -Dre2_ROOT="${TP_INSTALL_DIR}" \
    -DZLIB_SOURCE=SYSTEM \
    -DZLIB_LIBRARY="${TP_INSTALL_DIR}/lib/libz.a" -DZLIB_INCLUDE_DIR="${TP_INSTALL_DIR}/include" \
    -DRapidJSON_SOURCE=SYSTEM \
    -DRapidJSON_ROOT="${TP_INSTALL_DIR}" \
    -Dorc_ROOT="${TP_INSTALL_DIR}" \
    -Dxsimd_SOURCE=BUNDLED \
    -DBrotli_SOURCE=BUNDLED \
    -DARROW_LZ4_USE_SHARED=OFF \
    -DLz4_SOURCE=BUNDLED \
    -DARROW_ZSTD_USE_SHARED=OFF \
    -DZSTD_LIB="${TP_INSTALL_DIR}/lib/libzstd.a" -DZSTD_INCLUDE_DIR="${TP_INSTALL_DIR}/include" \
    -Dzstd_SOURCE=SYSTEM \
    -DSnappy_LIB="${TP_INSTALL_DIR}/lib/libsnappy.a" -DSnappy_INCLUDE_DIR="${TP_INSTALL_DIR}/include" \
    -DSnappy_SOURCE=SYSTEM \
    -DBoost_ROOT="${TP_INSTALL_DIR}" --no-warn-unused-cli \
    -DARROW_JEMALLOC=OFF -DARROW_MIMALLOC=OFF \
    -DJEMALLOC_HOME="${TP_INSTALL_DIR}" \
    -DARROW_THRIFT_USE_SHARED=OFF \
    -DThrift_SOURCE=SYSTEM \
    -DThrift_ROOT="${TP_INSTALL_DIR}" ..

echo "=== CMake configuration done, building... ==="

"${BUILD_SYSTEM}" -j "${PARALLEL}"

echo "=== Installing Arrow ==="
"${BUILD_SYSTEM}" install

# Copy bundled deps (brotli)
if [[ -d ./brotli_ep/src/brotli_ep-install/lib ]]; then
    cp -rf ./brotli_ep/src/brotli_ep-install/lib/libbrotlienc-static.a "${TP_INSTALL_DIR}/lib64/libbrotlienc.a"
    cp -rf ./brotli_ep/src/brotli_ep-install/lib/libbrotlidec-static.a "${TP_INSTALL_DIR}/lib64/libbrotlidec.a"
    cp -rf ./brotli_ep/src/brotli_ep-install/lib/libbrotlicommon-static.a "${TP_INSTALL_DIR}/lib64/libbrotlicommon.a"
fi

echo "=== Arrow MSAN rebuild complete ==="
echo "Libraries installed to ${TP_INSTALL_DIR}/lib64/"
ls -la "${TP_INSTALL_DIR}/lib64/libarrow"* 2>/dev/null
